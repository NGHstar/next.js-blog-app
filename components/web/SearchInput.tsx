import { Loader2, Search } from 'lucide-react'
import { Input } from '../ui/input'
import React, { useState } from 'react'
import { useQuery } from 'convex/react'
import { api } from '@/convex/_generated/api'
import Link from 'next/link'

function SearchInput() {
  // ---
  const [term, setTerm] = useState('')
  const [open, setOpen] = useState(false)

  const results = useQuery(api.posts.searchPosts, term.length >= 2 ? { limit: 5, term } : 'skip')

  function handleInputChange(e: React.ChangeEvent<HTMLInputElement>) {
    setTerm(e.target.value)
    setOpen(true)
  }

  return (
    <div className="relative w-full max-w-sm">
      <div className="relative">
        <Search className="absolute left-2.5 top-2.5 size-4 text-muted-foreground" />
        <Input
          type="search"
          placeholder="search posts ..."
          className="w-full pl-8 bg-background"
          value={term}
          onChange={handleInputChange}
        />
      </div>
      {open && term.length >= 2 && (
        <div
          className="absolute top-full mt-2 rounded-md border bg-popover text-popover-foreground shadow-md
        outline-none animate-in fade-in-0 zoom-in-95 z-40
        "
        >
          {results === undefined ? (
            <div className="flex items-center justify-center py-3 px-4 text-sm text-muted-foreground">
              <Loader2 className="animate-spin mr-2 size-4" />
              loading...
            </div>
          ) : results.length === 0 ? (
            <p className="py-3 px-4 text-sm text-muted-foreground text-center">No results found!</p>
          ) : (
            <div>
              {results.map(post => {
                return (
                  <Link
                    className="flex flex-col px-4 py-2 text-sm hover:bg-accent  cursor-pointer rounded-md"
                    key={post._id}
                    href={`/blog/${post._id}`}
                    onClick={() => {
                      setOpen(false)
                      setTerm('')
                    }}
                  >
                    <p className="font-medium truncate">{post.title}</p>
                    <p className="text-xs text-muted-foreground">{post.content.substring(0, 50)}</p>
                  </Link>
                )
              })}
            </div>
          )}
        </div>
      )}
    </div>
  )
}

export default SearchInput
